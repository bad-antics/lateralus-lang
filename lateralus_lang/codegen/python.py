    def visit_StructDecl(self, node: StructDecl):
        if self._cfg_stripped(node.decorators):
            return
        derive_traits = self._get_derive_traits(node.decorators)
        for d in node.decorators:
            if d.name != "derive":
                self._w += self._render_decorator(d)
        self._w += "@_dataclass"
        if node.interfaces:
            bases = ", ".join(node.interfaces)
            self._w += f"class {node.name}({bases}):"
        else:
            self._w += f"class {node.name}:"
        with self._w.indent():
            if node.fields:
                for f in node.fields:
                    type_ann = f": {self._py_type(f.type_.name)}" if f.type_ else ""
                    default  = f" = {self._expr(f.default)}" if f.default else ""
                    self._w += f"{f.name}{type_ann}{default}"
            # Inject impl methods collected in the pre-scan
            for method in self._impl_map.get(node.name, []):
                self._w += ""
                self._emit_method(method)
            # v1.8 — @derive auto-generated methods
            if derive_traits:
                self._emit_derive_methods(node, derive_traits)
            if not node.fields and not self._impl_map.get(node.name) and not derive_traits:
                self._w += "pass"
        # Structural generator spec for @law verifier — preserves field names + types
        if node.fields:
            spec_parts = []
            for f in node.fields:
                tname = self._ltl_type_str(f.type_) if f.type_ else "any"
                spec_parts.append(f"({f.name!r}, {tname!r})")
            self._w += f"{node.name}._ltl_struct_spec = [{', '.join(spec_parts)}]"
            self._w += f"_LATERALUS_STRUCTS[{node.name!r}] = {node.name}"
        self._w += ""

    def visit_EnumDecl(self, node: EnumDecl):
        if self._cfg_stripped(node.decorators):
            return
        derive_traits = self._get_derive_traits(node.decorators)
        for d in node.decorators:
            if d.name != "derive":
                self._w += self._render_decorator(d)
        self._w += f"class {node.name}(_Enum):"
        with self._w.indent():
            if node.variants:
                for i, v in enumerate(node.variants):
                    if v.value:
                        self._w += f"{v.name} = {self._expr(v.value)}"
                    elif v.fields:
                        self._w += f"{v.name} = {i + 1}"
                    else:
                        self._w += f"{v.name} = {i + 1}"
            else:
                self._w += "pass"
            # Inject impl block methods collected in the pre-scan
            for method in self._impl_map.get(node.name, []):
                self._w += ""
                self._emit_method(method)
            # All Lateralus enums have a built-in to_str() method
            self._w += ""
            self._w += "def to_str(self):"
            with self._w.indent():
                self._w += "return self.name"
            if derive_traits:
                self._emit_derive_methods(node, derive_traits)
        self._w += ""

    def visit_TypeAlias(self, node: TypeAlias):
        # Skip aliases to primitive/builtin types that don't map to Python names
        _primitives = {'fn', 'int', 'float', 'bool', 'str', 'string', 'list', 'map', 'any', 'void', 'never', 'char', 'byte', 'u8', 'u16', 'u32', 'u64', 'i8', 'i16', 'i32', 'i64', 'f32', 'f64', 'usize', 'isize'}
        target = node.target.name if hasattr(node.target, 'name') else str(node.target)
        if target in _primitives:
            return
        self._w += f"{node.name} = {target}"
        self._w += ""

    def visit_ImplBlock(self, node: ImplBlock):
        # Methods already injected into StructDecl via pre-scan.
        # For impl without a matching struct, patch the class.
        if node.type_name not in self._impl_map:
            return
        # Already emitted alongside the struct — skip standalone emit.

    def visit_InterfaceDecl(self, node: InterfaceDecl):
        bases = ", ".join(node.extends) if node.extends else "_ABC"
        self._w += f"class {node.name}({bases}):"
        with self._w.indent():
            if node.methods:
                for m in node.methods:
                    params = self._render_params(m.params)
                    kw = "async def" if m.is_async else "def"
                    self._w += "@_abstractmethod"
                    self._w += f"{kw} {m.name}({params}):"
                    with self._w.indent():
                        self._w += "..."
            else:
                self._w += "pass"
        self._w += ""

    # ── v1.2: polyglot foreign block ──────────────────────────────────────────

    def visit_ForeignBlock(self, node: ForeignBlock):
        """Emit an inline polyglot call."""
        if node.params:
            kv = ", ".join(f'"{p.name}": {self._expr(p.value)}' for p in node.params)
            params_expr = f"{{{kv}}}"
        else:
            params_expr = "{}"
        escaped = node.source.replace("\\", "\\\\").replace('"""', '\\"\\"\\"')
        self._w += f'_foreign_result = _get_polyglot().eval({node.lang!r}, """{escaped}""", {params_expr})'

    def _produces_result(self, stmt) -> bool:
        """True if stmt generates `_result = …` (value-arm match / if-expr etc.)
        and thus needs an implicit `return _result` appended."""
        if isinstance(stmt, MatchStmt):
            return bool(stmt.arms) and all(a.value is not None for a in stmt.arms)
        return False

    def _emit_fn_body(self, body) -> None:
        """Emit a function/method body and append `return _result` if needed."""
        body.accept(self)
        if body.stmts and self._produces_result(body.stmts[-1]):
            self._w += "return _result"

    def _emit_method(self, m: FnDecl) -> None:
        """Emit a function declaration inside a class body."""
        params = self._render_params(m.params)
        kw = "async def" if m.is_async else "def"
        self._w += f"{kw} {m.name}({params}):"
        if m.body and m.body.stmts:
            with self._w.indent():
                self._emit_fn_body(m.body)
        else:
            with self._w.indent():
                self._w += "pass"

    def _render_decorator(self, d: Decorator) -> str:
        # Map Lateralus decorator names to Python equivalents
        _deco_map = {
            "memo":       "memo",
            "typed":      "typed",
            "test":       "test",
            "law":        "law",
            "dataclass":  "_dataclass",
            "abstract":   "_abstractmethod",
            "staticmethod": "staticmethod",
            "classmethod":  "classmethod",
            "property":     "property",
            "deprecated": "_deprecated",   # shim: just a print-on-call wrapper
            "inline":     "",              # no-op in Python
        }
        py_name = _deco_map.get(d.name, d.name)
        if not py_name:
            return f"# @{d.name} (no-op)"
        if d.args or d.kwargs:
            parts = [self._expr(a) for a in d.args]
            parts += [f"{k}={self._expr(v)}" for k, v in d.kwargs]
            return f"@{py_name}({', '.join(parts)})"
        return f"@{py_name}"

    # ── expression emitter ────────────────────────────────────────────────────

    def _expr(self, node: Optional[Node]) -> str:
        if node is None:
            return "None"
        if isinstance(node, Literal):
            if node.kind == "nil":    return "None"
            if node.kind == "bool":   return "True" if node.value else "False"
            if node.kind == "str":    return repr(node.value)
            return repr(node.value)
        if isinstance(node, Ident):
            return node.name
        if isinstance(node, BinOp):
            if node.op == "|>":
                left = self._expr(node.left)
                # If RHS is a CallExpr, inject LHS as first argument (partial application)
                # BUT if the callee is a known impl method, use method-call syntax instead.
                if isinstance(node.right, CallExpr):
                    fn   = self._expr(node.right.callee)
                    rest = [self._expr(a) for a in node.right.args]
                    kw   = [f"{k}={self._expr(v)}" for k, v in node.right.kwargs]
                    # Detect impl methods: use method-call syntax (left.fn(args))
                    fn_name = node.right.callee.name if isinstance(node.right.callee, Ident) else None
                    is_impl_method = fn_name and any(
                        fn_name == m.name
                        for methods in self._impl_map.values()
                        for m in methods
                    )
                    if is_impl_method:
                        args_str = ", ".join(rest + kw)
                        return f"{left}.{fn}({args_str})"
                    all_args = ", ".join([left] + rest + kw)
                    return f"{fn}({all_args})"
                return f"{self._expr(node.right)}({left})"
            if node.op == "|?":
                # Optional pipeline: skip nil/None values
                left = self._expr(node.left)
                tmp = "_opt_val"
                if isinstance(node.right, CallExpr):
                    fn   = self._expr(node.right.callee)
                    rest = [self._expr(a) for a in node.right.args]
                    kw   = [f"{k}={self._expr(v)}" for k, v in node.right.kwargs]
                    all_args = ", ".join([tmp] + rest + kw)
                    return f"(({tmp} := {left}) is not None and {fn}({all_args}) or None)"
                return f"(({tmp} := {left}) is not None and {self._expr(node.right)}({tmp}) or None)"
            py_op = {"&&": "and", "||": "or", "**": "**",
                     "and": "and", "or": "or"}.get(node.op, node.op)
            return f"({self._expr(node.left)} {py_op} {self._expr(node.right)})"
        if isinstance(node, UnaryOp):
            py_op = {"!": "not ", "not": "not ", "-": "-", "~": "~",
                     "typeof": "type(", "sizeof": "len("}.get(node.op, node.op)
            if node.op in ("typeof", "sizeof"):
                return f"{py_op}{self._expr(node.operand)})"
            return f"{py_op}{self._expr(node.operand)}"
        if isinstance(node, CallExpr):
            args = ", ".join(self._expr(a) for a in node.args)
            kwargs = ", ".join(f"{k}={self._expr(v)}" for k, v in node.kwargs)
            all_args = ", ".join(filter(None, [args, kwargs]))
            return f"{self._expr(node.callee)}({all_args})"
        if isinstance(node, IndexExpr):
            return f"{self._expr(node.obj)}[{self._expr(node.index)}]"
        if isinstance(node, FieldExpr):
            return f"{self._expr(node.obj)}.{node.field}"
        if isinstance(node, LambdaExpr):
            params = self._render_lambda_params(node.params)
            if node.body:
                return f"lambda {params}: {self._expr(node.body)}"
            # Block lambda: fn(x) { ... }
            if node.block:
                stmts = node.block.stmts if isinstance(node.block, BlockStmt) else [node.block]
                # Single expression statement → inline lambda
                if len(stmts) == 1 and isinstance(stmts[0], ExprStmt):
                    return f"lambda {params}: {self._expr(stmts[0].expr)}"
                if len(stmts) == 1 and isinstance(stmts[0], ReturnStmt) and stmts[0].value:
                    return f"lambda {params}: {self._expr(stmts[0].value)}"
                # Multi-statement → hoist to a named function
                self._lambda_counter += 1
                fn_name = f"_ltl_lambda_{self._lambda_counter}"
                # Build the hoisted function source using a temporary writer
                saved = self._w
                self._w = _W()
                self._w += f"def {fn_name}({params}):"
                with self._w.indent():
                    for s in stmts:
                        s.accept(self)
                hoisted_code = self._w.getvalue()
                self._w = saved
                self._hoisted_lambdas.append(hoisted_code)
                return fn_name
            return f"lambda {params}: None  # block lambda"
        if isinstance(node, ListExpr):
            return f"[{', '.join(self._expr(e) for e in node.elements)}]"
        if isinstance(node, MapExpr):
            pairs = ", ".join(f"{self._expr(k)}: {self._expr(v)}"
                              for k, v in node.pairs)
            return "{" + pairs + "}"
        if isinstance(node, TupleExpr):
            return f"({', '.join(self._expr(e) for e in node.elements)},)"
        if isinstance(node, AwaitExpr):
            return f"await {self._expr(node.value)}"
        if isinstance(node, CastExpr):
            return f"{node.target.name}({self._expr(node.value)})"
        if isinstance(node, RangeExpr):
            end_expr = self._expr(node.end)
            adj = " + 1" if node.inclusive else ""
            return f"range({self._expr(node.start)}, {end_expr}{adj})"
        if isinstance(node, InterpolatedStr):
            parts = []
            for kind, val in node.parts:
                if kind == "str":
                    parts.append(val.replace("{", "{{").replace("}", "}}"))
                else:
                    # Swap " → ' inside expression slots to avoid f-string conflicts
                    safe_val = val.replace('"', "'")
                    parts.append("{" + safe_val + "}")
            return 'f"' + "".join(parts) + '"'
        # v1.1 expressions
        if isinstance(node, SelfExpr):
            return "self"
        if isinstance(node, StructLiteral):
            fields = ", ".join(f"{k}={self._expr(v)}" for k, v in node.fields)
            return f"{node.name}({fields})"
        if isinstance(node, YieldExpr):
            val = self._expr(node.value) if node.value else ""
            return f"(yield {val})"
        if isinstance(node, SpawnExpr):
            return f"_asyncio.ensure_future({self._expr(node.call)})"
        # v1.6 concurrency expressions
        if isinstance(node, ChannelExpr):
            cap = self._expr(node.capacity) if node.capacity else "0"
            return f"_Channel({cap})"
        if isinstance(node, CancelExpr):
            return "_CancellationToken()"
        if isinstance(node, ParallelExpr):
            items = self._expr(node.items)
            func = self._expr(node.func)
            if node.kind == "map":
                return f"_parallel_map({func}, {items})"
            elif node.kind == "filter":
                return f"_parallel_filter({func}, {items})"
            elif node.kind == "reduce":
                init = self._expr(node.init) if node.init else "None"
                return f"_parallel_reduce({func}, {items}, {init})"
            return f"_parallel_map({func}, {items})"
        if isinstance(node, BlockStmt):
            # Empty block used as an expression = empty map literal {}
            if not node.stmts:
                return "{}"
            # Inline block — render as immediately invoked lambda
            stmts = "\n    ".join(self._stmt_str(s) for s in node.stmts)
            return f"(lambda: ({stmts}))()"
        # v1.3 expressions
        if isinstance(node, ProbeExpr):
            inner = self._expr(node.value)
            return (f"(lambda _v: {{'type': type(_v).__name__, 'value': _v, "
                    f"'len': len(_v) if hasattr(_v, '__len__') else None}})({inner})")
        if isinstance(node, TryExpr):
            # try { ... } recover EType(e) { ... } as expression
            # Emitted as: (_ltl_try_helper function defined inline)
            # We use a nested lambda approach via exec — simpler: a named helper
            # emitted just before the enclosing statement (handled at stmt level)
            # For now, wrap in a self-calling closure using the statement visitor
            return self._try_expr_to_iife(node)
        if isinstance(node, PropagateExpr):
            return f"_ltl_propagate({self._expr(node.value)})"
        if isinstance(node, ChainExpr):
            return f"({self._expr(node.error)}).caused_by({self._expr(node.cause)})"
        if isinstance(node, ComprehensionExpr):
            cond = f" if {self._expr(node.condition)}" if node.condition else ""
            if node.kind == "set":
                return f"{{{self._expr(node.expr)} for {node.var} in {self._expr(node.iter)}{cond}}}"
            elif node.kind == "map":
                return f"{{{self._expr(node.expr)} for {node.var} in {self._expr(node.iter)}{cond}}}"
            return f"[{self._expr(node.expr)} for {node.var} in {self._expr(node.iter)}{cond}]"
        if isinstance(node, GuardExpr):
            return f"(None if not ({self._expr(node.condition)}) else True)"
        if isinstance(node, WhereClause):
            binds = "; ".join(self._stmt_str(s) for s in node.bindings.stmts)
            return f"(lambda: ({binds}, {self._expr(node.expr)})[-1])()"
        if isinstance(node, SpreadExpr):
            return f"*{self._expr(node.value)}"
        if isinstance(node, TernaryExpr):
            return f"({self._expr(node.then_val)} if {self._expr(node.condition)} else {self._expr(node.else_val)})"
        if isinstance(node, TernaryExpr):
            return f"({self._expr(node.then_val)} if {self._expr(node.condition)} else {self._expr(node.else_val)})"
        # v1.5 — ResultExpr: Result::Ok(v) → Ok(v), Result::Err(e) → Err(e)
        if isinstance(node, ResultExpr):
            val = self._expr(node.value) if node.value is not None else "None"
            return f"{node.variant}({val})"
        # v1.5 — OptionExpr: Option::Some(v) → Some(v), Option::None → None_
        if isinstance(node, OptionExpr):
            if node.variant == "None" or node.value is None:
                return "None_"
            return f"Some({self._expr(node.value)})"
        # v1.5 — TypeMatchExpr used as value expression → IIFE
        if isinstance(node, TypeMatchExpr):
            return self._type_match_expr_iife(node)
        # v1.5 — Pattern nodes referenced directly as expressions (rare, e.g. in _expr fallback)
        if isinstance(node, WildcardPattern):
            return "_"
        if isinstance(node, BindingPattern):
            return node.name
        if isinstance(node, LiteralPattern):
            return self._expr(node.value)
        if isinstance(node, EnumVariantPattern):
            if node.fields:
                fields = ", ".join(self._render_type_pattern(f) for f in node.fields)
                return f"{node.variant_name}({fields})"
            return node.variant_name
        # v1.8 — metaprogramming expression nodes
        if isinstance(node, MacroInvocation):
            args = ", ".join(self._expr(a) for a in node.args)
            return f"_macro_{node.name}({args})"
        if isinstance(node, ReflectExpr):
            return f'_type_info("{node.target}")'
        if isinstance(node, QuoteExpr):
            stmts = []
            if hasattr(node.body, 'stmts'):
                for s in node.body.stmts:
                    stmts.append(repr(type(s).__name__))
            return '{"__ast__": [' + ", ".join(stmts) + ']}'
        if isinstance(node, UnquoteExpr):
            return self._expr(node.expr)
        return "None"

    def _stmt_str(self, node) -> str:
        w = _W()
        old_w = self._w
        self._w = w
        node.accept(self)
        self._w = old_w
        return w.getvalue().strip()

    def _try_expr_to_iife(self, node: TryExpr) -> str:
        """Emit try { ... } recover ... as an immediately-invoked helper function.

        The last ExprStmt in each branch becomes the implicit return value,
        captured via ``_ltl_r``:

            def _ltl_try_N():
                _ltl_r = None
                try:
                    <body stmts>
                    _ltl_r = <last-expr if ExprStmt>
                except ExcType as binding:
                    <recovery stmts>
                    _ltl_r = <last-expr if ExprStmt>
                finally:
                    <ensure stmts>
                return _ltl_r
            result = _ltl_try_N()
        """
        _exc_map = {
            "Error":          "Exception",
            "RuntimeError":   "RuntimeError",
            "ValueError":     "ValueError",
            "TypeError":      "TypeError",
            "IndexError":     "IndexError",
            "KeyError":       "KeyError",
            "IOError":        "IOError",
            "DivisionError":  "ZeroDivisionError",
            "OverflowError":  "OverflowError",
            "NotFoundError":  "FileNotFoundError",
            "AssertionError": "AssertionError",
        }

        if not hasattr(self, '_try_counter'):
            self._try_counter = 0
        self._try_counter += 1
        fn_name = f"_ltl_try_{self._try_counter}"

        def emit_block_with_capture(block: "BlockStmt", w: _W):
            """Emit block stmts; last ExprStmt sets _ltl_r."""
            stmts = block.stmts if block else []
            for i, stmt in enumerate(stmts):
                is_last = (i == len(stmts) - 1)
                if is_last and isinstance(stmt, ExprStmt):
                    w += f"_ltl_r = {self._expr(stmt.expr)}"
                elif is_last and isinstance(stmt, ReturnStmt):
                    # Return becomes: set _ltl_r then return
                    if stmt.value:
                        w += f"_ltl_r = {self._expr(stmt.value)}"
                    w += "return _ltl_r"
                else:
                    old_w = self._w
                    self._w = w
                    stmt.accept(self)
                    self._w = old_w

        self._w += f"def {fn_name}():"
        with self._w.indent():
            self._w += "_ltl_r = None"
            self._w += "try:"
            with self._w.indent():
                emit_block_with_capture(node.body, self._w)
            for clause in node.recoveries:
                py_exc = _exc_map.get(clause.error_type,
                                      clause.error_type or "Exception") \
                         if clause.error_type else "Exception"
                exc_decl = f"{py_exc} as {clause.binding}" if clause.binding else py_exc
                self._w += f"except {exc_decl}:"
                with self._w.indent():
                    emit_block_with_capture(clause.body, self._w)
            if node.ensure:
                self._w += "finally:"
                with self._w.indent():
                    old_w = self._w
                    node.ensure.accept(self)
            self._w += "return _ltl_r"
        self._w += ""
        return f"{fn_name}()"

    def _type_match_expr_iife(self, node: TypeMatchExpr) -> str:
        """Emit a TypeMatchExpr used as a value — wraps match/case in a lambda IIFE.

        let x = match v { Result::Ok(n) => n, _ => 0 }
            →
        def _ltl_tmatch_N():
            match v:
                case _LtlOk(n):
                    return n
                case _:
                    return 0
        x = _ltl_tmatch_N()
        """
        if not hasattr(self, '_tmatch_counter'):
            self._tmatch_counter = 0
        self._tmatch_counter += 1
        fn_name = f"_ltl_tmatch_{self._tmatch_counter}"
        subject = self._expr(node.subject)

        self._w += f"def {fn_name}():"
        with self._w.indent():
            if self._use_match:
                self._w += f"match {subject}:"
                with self._w.indent():
                    for arm in node.arms:
                        pat = self._render_type_pattern(arm.pattern)
                        guard = f" if {self._expr(arm.guard)}" if arm.guard else ""
                        self._w += f"case {pat}{guard}:"
                        with self._w.indent():
                            if arm.body:
                                # last ExprStmt becomes return value
                                stmts = arm.body.stmts if arm.body else []
                                for i, stmt in enumerate(stmts):
                                    if i == len(stmts) - 1 and isinstance(stmt, ExprStmt):
                                        self._w += f"return {self._expr(stmt.expr)}"
                                    else:
                                        old_w = self._w; self._w = self._w
                                        stmt.accept(self)
                            elif arm.value:
                                self._w += f"return {self._expr(arm.value)}"
                            else:
                                self._w += "return None"
            else:
                self._w += f"_s = {subject}"
                first = True
                for arm in node.arms:
                    pat = self._render_type_pattern(arm.pattern)
                    guard = f" and ({self._expr(arm.guard)})" if arm.guard else ""
                    kw = "if" if first else "elif"
                    self._w += f"{kw} True{guard}:  # pattern: {pat}"
                    with self._w.indent():
                        if arm.value:
                            self._w += f"return {self._expr(arm.value)}"
                        else:
                            self._w += "return None"
                    first = False
            self._w += "return None"
        self._w += ""
        return f"{fn_name}()"

    def generic_visit(self, node: Node):
        pass  # silently skip unknown nodes in expression context

    # ── v1.6 concurrency visitors ─────────────────────────────────────────

    def visit_SelectStmt(self, node: SelectStmt):
        """
        select { msg from ch => { ... } ... }
        →  Python polling loop using try_recv / try_send
        """
        self._w += "# select"
        self._w += "import time as _time"
        self._w += "_sel_done = False"
        self._w += "while not _sel_done:"
        with self._w.indent():
            for arm in node.arms:
                if arm.kind == "recv":
                    ch = self._expr(arm.channel)
                    self._w += f"_sel_val = {ch}.try_recv()"
                    self._w += "if _sel_val is not None:"
                    with self._w.indent():
                        if arm.binding:
                            self._w += f"{arm.binding} = _sel_val"
                        arm.body.accept(self)
                        self._w += "_sel_done = True"
                        self._w += "break"
                elif arm.kind == "send":
                    ch = self._expr(arm.channel)
                    val = self._expr(arm.value)
                    self._w += f"if {ch}.try_send({val}):"
                    with self._w.indent():
                        arm.body.accept(self)
                        self._w += "_sel_done = True"
                        self._w += "break"
                elif arm.kind == "timeout":
                    ms = self._expr(arm.value)
                    # emit a one-shot timeout check using monotonic clock
                    self._w += "if not hasattr(_sel_done, '_sel_t0'):"
                    with self._w.indent():
                        self._w += "_sel_t0 = _time.monotonic()"
                    self._w += f"if (_time.monotonic() - _sel_t0) * 1000 >= {ms}:"
                    with self._w.indent():
                        arm.body.accept(self)
                        self._w += "_sel_done = True"
                        self._w += "break"
                elif arm.kind == "default":
                    arm.body.accept(self)
                    self._w += "_sel_done = True"
                    self._w += "break"
            self._w += "_time.sleep(0.001)"

    def visit_NurseryBlock(self, node: NurseryBlock):
        """nursery { ... } → with Nursery() as _nursery: ..."""
        self._w += "from lateralus_lang.async_runtime import Nursery as _Nursery"
        name = node.name or "_nursery"
        self._w += f"with _Nursery() as {name}:"
        with self._w.indent():
            if node.body:
                node.body.accept(self)
            else:
                self._w += "pass"

    def visit_AsyncForStmt(self, node: AsyncForStmt):
        """async for x in stream { ... } → async for x in stream: ..."""
        self._w += f"async for {node.var} in {self._expr(node.iter)}:"
        with self._w.indent():
            if node.body:
                node.body.accept(self)
            else:
                self._w += "pass"

    # ── v1.7 — Conditional Compilation ──────────────────────────────────

    def visit_CfgExpr(self, node: CfgExpr):
        """cfg!(key, "value") → True/False based on build context."""
        # If a CfgContext is set on the transpiler, evaluate at compile time;
        # otherwise default to True.
        if hasattr(self, '_cfg_context') and self._cfg_context:
            val = self._cfg_context.evaluate(node.key, node.value)
        else:
            val = True
        return "True" if val else "False"

    # ── v1.8 — Metaprogramming ─────────────────────────────────────────

    def visit_ConstFnDecl(self, node: ConstFnDecl):
        """const fn → regular def with a @_const_fn marker comment.

        In a full implementation the compiler would evaluate at compile time
        and inline results.  For the Python target we emit a normal function
        wrapped in a cache so repeated calls are O(1).
        """
        params_str = self._render_params(node.params)
        self._w += "# [const fn] — evaluated at compile time"
        self._w += "@_lru_cache(maxsize=None)"
        self._w += f"def {node.name}({params_str}):"
        with self._w.indent():
            if node.body:
                node.body.accept(self)
            else:
                self._w += "pass"
        self._w += ""

    def visit_MacroDecl(self, node: MacroDecl):
        """macro name!(...) { body } → stored in _macro_registry for expansion.

        At the Python level we emit the macro as a regular function whose
        parameters are the macro arguments.
        """
        params_str = ", ".join(node.params) if node.params else ""
        self._w += f"# [macro] {node.name}!"
        self._w += f"def _macro_{node.name}({params_str}):"
        with self._w.indent():
            if node.body:
                node.body.accept(self)
            else:
                self._w += "pass"
        self._w += ""

    def visit_MacroInvocation(self, node: MacroInvocation):
        """name!(args) → call the expanded macro function."""
        args_str = ", ".join(self._expr(a) for a in node.args)
        return f"_macro_{node.name}({args_str})"

    def visit_CompTimeBlock(self, node: CompTimeBlock):
        """comptime { stmts } → inline the block as regular code with a marker."""
        self._w += "# [comptime] — compile-time evaluated block"
        if node.body:
            node.body.accept(self)

    def visit_ReflectExpr(self, node: ReflectExpr):
        """reflect!(Type) → _type_info("Type") at runtime."""
        return f'_type_info("{node.target}")'

    def visit_QuoteExpr(self, node: QuoteExpr):
        """quote { expr } → string representation of the AST fragment."""
        # In a full implementation this would produce an AST object.
        # For Python target we emit a dict describing the block.
        if node.body and hasattr(node.body, 'stmts') and node.body.stmts:
            # Capture inner expressions as string literals
            parts = []
            for stmt in node.body.stmts:
                parts.append(self._expr(stmt) if hasattr(stmt, 'accept') else repr(stmt))
            return '{"__ast__": [' + ", ".join(parts) + ']}'
        return '{"__ast__": []}'

    def visit_UnquoteExpr(self, node: UnquoteExpr):
        """$ident / ${expr} → splice the value directly."""
        return self._expr(node.expr) if node.expr else '""'

    def _get_derive_traits(self, decorators) -> list[str]:
        """Extract trait names from @derive(Trait1, Trait2, ...) decorators."""
        traits: list[str] = []
        for d in (decorators or []):
            if d.name == "derive":
                for arg in d.args:
                    if isinstance(arg, Ident):
                        traits.append(arg.name)
                    elif isinstance(arg, Literal) and isinstance(arg.value, str):
                        traits.append(arg.value)
        return traits

    def _emit_derive_methods(self, node, traits: list[str]):
        """Auto-generate methods for @derive(...) traits on a struct/enum."""
        name = node.name
        fields = getattr(node, 'fields', []) or []
        field_names = [f.name for f in fields]

        for trait in traits:
            trait_lower = trait.lower()
            if trait_lower == "debug":
                self._w += ""
                self._w += "def __repr__(self):"
                with self._w.indent():
                    if field_names:
                        parts = ", ".join(f'{fn}={{self.{fn}!r}}' for fn in field_names)
                        self._w += f'return f"{name}({parts})"'
                    else:
                        self._w += f'return "{name}()"'

            elif trait_lower == "display":
                self._w += ""
                self._w += "def __str__(self):"
                with self._w.indent():
                    if field_names:
                        parts = ", ".join(f'{{self.{fn}}}' for fn in field_names)
                        self._w += f'return f"{name}({parts})"'
                    else:
                        self._w += f'return "{name}"'

            elif trait_lower == "eq":
                self._w += ""
                self._w += "def __eq__(self, other):"
                with self._w.indent():
                    self._w += f"if not isinstance(other, {name}):"
                    with self._w.indent():
                        self._w += "return NotImplemented"
                    if field_names:
                        conds = " and ".join(f"self.{fn} == other.{fn}" for fn in field_names)
                        self._w += f"return {conds}"
                    else:
                        self._w += "return True"

            elif trait_lower == "hash":
                self._w += ""
                self._w += "def __hash__(self):"
                with self._w.indent():
                    if field_names:
                        tup = ", ".join(f"self.{fn}" for fn in field_names)
                        self._w += f"return hash(({tup},))"
                    else:
                        self._w += f"return hash('{name}')"

            elif trait_lower == "clone":
                self._w += ""
                self._w += "def clone(self):"
                with self._w.indent():
                    if field_names:
                        args = ", ".join(f"{fn}=self.{fn}" for fn in field_names)
                        self._w += f"return {name}({args})"
                    else:
                        self._w += f"return {name}()"

            elif trait_lower == "default":
                self._w += ""
                self._w += "@classmethod"
                self._w += "def default(cls):"
                with self._w.indent():
                    if field_names:
                        defaults = []
                        for f in fields:
                            if f.default:
                                defaults.append(f"{f.name}={self._expr(f.default)}")
                            elif f.type_ and hasattr(f.type_, 'name'):
                                tname = f.type_.name.lower()
                                if tname in ("int", "float"):
                                    defaults.append(f"{f.name}=0")
                                elif tname == "str":
                                    defaults.append(f'{f.name}=""')
                                elif tname == "bool":
                                    defaults.append(f"{f.name}=False")
                                elif tname in ("list", "array"):
                                    defaults.append(f"{f.name}=[]")
                                else:
                                    defaults.append(f"{f.name}=None")
                            else:
                                defaults.append(f"{f.name}=None")
                        self._w += f"return cls({', '.join(defaults)})"
                    else:
                        self._w += "return cls()"

            elif trait_lower == "serialize":
                self._w += ""
                self._w += "def to_dict(self):"
                with self._w.indent():
                    if field_names:
                        self._w += "return {"
                        with self._w.indent():
                            for fn in field_names:
                                self._w += f'"{fn}": self.{fn},'
                        self._w += "}"
                    else:
                        self._w += "return {}"

            elif trait_lower == "deserialize":
                self._w += ""
                self._w += "@classmethod"
                self._w += "def from_dict(cls, data):"
                with self._w.indent():
                    if field_names:
                        args = ", ".join(f'{fn}=data.get("{fn}")' for fn in field_names)
                        self._w += f"return cls({args})"
                    else:
                        self._w += "return cls()"


