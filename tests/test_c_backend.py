class TestImportStmt:
    """Test import statement handling — imports are resolved before codegen,
    so they don't appear in the AST body. Test that the visitor exists and
    works when called directly."""

    def test_import_visitor_exists(self):
        """The CTranspiler should have an ImportStmt visitor."""
        t = CTranspiler()
        assert hasattr(t, "_visit_ImportStmt")

    def test_import_compiles_without_error(self):
        """Programs with imports should still compile to C."""
        c_src = check_c("import io\nfn test() { }")
        assert "test" in c_src

    def test_direct_import_visit(self):
        """Test ImportStmt visitor when called directly."""
        from lateralus_lang.ast_nodes import ImportStmt
        t = CTranspiler()
        t._w = __import__("lateralus_lang.codegen.c", fromlist=["_W"])._W()
        t._visit_ImportStmt(ImportStmt(path="io"))
        result = t._w.result()
        assert "import" in result


