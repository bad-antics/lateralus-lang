# --- Helpers -------------------------------------------------------------------

def compile_c(src: str, *, freestanding: bool = False) -> CompileResult:
    """Compile Lateralus source to C."""
    c = Compiler()
    if freestanding:
        c.freestanding = True
    return c.compile_source(src, target=Target.C, filename="<test>")


def check_c(src: str, *, freestanding: bool = False) -> str:
    """Compile and return C source, asserting success."""
    r = compile_c(src, freestanding=freestanding)
    assert r.ok, f"Compilation failed: {[e.message for e in r.errors[:3]]}"
    assert r.c_src is not None, "No C source generated"
    return r.c_src


HAS_GCC = shutil.which("gcc") is not None


def gcc_syntax_check(c_code: str, freestanding: bool = False):
    """Run gcc -fsyntax-only on the generated C to catch syntax errors."""
    args = ["gcc", "-fsyntax-only", "-std=c99", "-x", "c", "-"]
    if freestanding:
        args.insert(1, "-ffreestanding")
        args.insert(2, "-nostdlib")
    result = subprocess.run(args, input=c_code, capture_output=True, text=True)
    return result


