"""
lateralus_lang/ir.py  ─  LATERALUS Intermediate Representation
═══════════════════════════════════════════════════════════════════════════
Three-address code (TAC) IR used between the AST and code generators.

IR Instructions
───────────────
  Each IRInstr holds:
    op      — IROp enum
    dest    — destination temporary or label (str)
    src1    — first operand (str, int, float, bool, None)
    src2    — second operand

  Function is the unit of compilation.
  A Function contains a list of BasicBlock.
  A BasicBlock contains a list of IRInstr.

Temporary naming
────────────────
  Temporaries: %0, %1, %2, …  (generated by IRBuilder)
  Labels:      L0, L1, L2, …
  Globals:     @name
  Parameters:  $name

Semantic analysis
─────────────────
  SemanticAnalyzer walks the AST, builds the symbol table (scope chain),
  infers types, and emits IR.  Type errors are collected (non-fatal) and
  surfaced through the error bridge.
═══════════════════════════════════════════════════════════════════════════
"""
from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum, auto
from typing import Any, Dict, List, Optional, Tuple

