    def visit_MatchStmt(self, node: MatchStmt):
        subj_tmp    = node.subject.accept(self)
        after_label = self._builder.new_label("match_after")
        for arm in node.arms:
            next_label = self._builder.new_label("match_arm")
            pat_tmp = arm.pattern.accept(self)
            cmp_tmp = self._builder.new_tmp()
            self._emit(IROp.EQ, dest=cmp_tmp, src1=subj_tmp, src2=pat_tmp)
            self._emit(IROp.JF, dest=next_label, src1=cmp_tmp)
            if arm.body:
                arm.body.accept(self)
            elif arm.value:
                arm.value.accept(self)
            self._emit(IROp.JMP, dest=after_label)
            arm_bb = self._new_bb(next_label)
            self._current_bb = arm_bb
            self._emit(IROp.LABEL, dest=next_label)
        after_bb = self._new_bb(after_label)
        self._current_bb = after_bb
        self._emit(IROp.LABEL, dest=after_label)

    def generic_visit(self, node: Node):
        # Walk children for nodes not explicitly handled
        for val in vars(node).values():
            if isinstance(val, Node):
                val.accept(self)
            elif isinstance(val, list):
                for item in val:
                    if isinstance(item, Node):
                        item.accept(self)

