class _W:
    """Simple indented code writer."""

    def __init__(self, indent_size: int = 4):
        self._lines: List[str] = []
        self._indent = 0
        self._indent_size = indent_size

    def line(self, code: str = "") -> None:
        if code:
            self._lines.append(" " * self._indent + code)
        else:
            self._lines.append("")

    def indent(self):  return _IndentCtx(self)
    def __iadd__(self, code: str):
        self.line(code); return self

    def getvalue(self) -> str:
        return "\n".join(self._lines)


class _IndentCtx:
    def __init__(self, w: _W): self.w = w
    def __enter__(self): self.w._indent += self.w._indent_size
    def __exit__(self, *_): self.w._indent -= self.w._indent_size


