    # ── v1.5 statement visitors ────────────────────────────────────────────────

    def visit_TypeMatchExpr(self, node: TypeMatchExpr):
        """match subject { Pat(v) => expr, ... }  →  Python match/case or if-chain."""
        subject = self._expr(node.subject)
        if self._use_match:
            self._w += f"match {subject}:"
            with self._w.indent():
                for arm in node.arms:
                    pat = self._render_type_pattern(arm.pattern)
                    guard = f" if {self._expr(arm.guard)}" if arm.guard else ""
                    self._w += f"case {pat}{guard}:"
                    with self._w.indent():
                        if arm.body:
                            arm.body.accept(self)
                        elif arm.value:
                            self._w += self._expr(arm.value)
                        else:
                            self._w += "pass"
        else:
            # Fallback: assign to temp, compare with if-chain
            self._w += f"_tmatch = {subject}"
            first = True
            for arm in node.arms:
                pat = self._render_type_pattern(arm.pattern)
                guard = f" and ({self._expr(arm.guard)})" if arm.guard else ""
                kw = "if" if first else "elif"
                self._w += f"{kw} isinstance(_tmatch, object){guard}:  # pattern: {pat}"
                with self._w.indent():
                    if arm.body:
                        arm.body.accept(self)
                    elif arm.value:
                        self._w += self._expr(arm.value)
                    else:
                        self._w += "pass"
                first = False

    def _render_type_pattern(self, pattern) -> str:
        """Render a v1.5 Pattern node as a Python match/case pattern string."""
        if isinstance(pattern, WildcardPattern):
            return "_"
        if isinstance(pattern, BindingPattern):
            return pattern.name
        if isinstance(pattern, LiteralPattern):
            return self._expr(pattern.value)
        if isinstance(pattern, TypePattern):
            if pattern.fields:
                fields = ", ".join(self._render_type_pattern(f) for f in pattern.fields)
                return f"{pattern.type_name}({fields})"
            return pattern.type_name
        if isinstance(pattern, EnumVariantPattern):
            # Result::Ok(v) → Ok(v), Result::Err(e) → Err(e), Option::Some(v) → Some(v)
            cls = pattern.variant_name
            if pattern.fields:
                fields = ", ".join(self._render_type_pattern(f) for f in pattern.fields)
                return f"{cls}({fields})"
            return cls
        if isinstance(pattern, TuplePattern):
            elements = ", ".join(self._render_type_pattern(e) for e in pattern.elements)
            return f"({elements},)" if len(pattern.elements) != 1 else f"({elements},)"
        if isinstance(pattern, ListPattern):
            parts = [self._render_type_pattern(h) for h in pattern.head]
            if pattern.rest:
                parts.append(f"*{pattern.rest}")
            return f"[{', '.join(parts)}]"
        if isinstance(pattern, OrPattern):
            return f"{self._render_type_pattern(pattern.left)} | {self._render_type_pattern(pattern.right)}"
        # Fallback: render as expression
        return self._expr(pattern)

