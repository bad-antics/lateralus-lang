    def visit_IfStmt(self, node: IfStmt):
        self._w += f"if {self._expr(node.condition)}:"
        with self._w.indent():
            node.then_block.accept(self)
        for ec, eb in node.elif_arms:
            self._w += f"elif {self._expr(ec)}:"
            with self._w.indent():
                eb.accept(self)
        if node.else_block:
            self._w += "else:"
            with self._w.indent():
                node.else_block.accept(self)

    def visit_MatchStmt(self, node: MatchStmt):
        if self._use_match:
            self._w += f"match {self._expr(node.subject)}:"
            with self._w.indent():
                for arm in node.arms:
                    pat = self._expr(arm.pattern)
                    guard = f" if {self._expr(arm.guard)}" if arm.guard else ""
                    self._w += f"case {pat}{guard}:"
                    with self._w.indent():
                        if arm.body:
                            arm.body.accept(self)
                        elif arm.value:
                            self._w += f"_result = {self._expr(arm.value)}"
                        else:
                            self._w += "pass"
        else:
            # Fallback for Python < 3.10
            first = True
            for arm in node.arms:
                kw = "if" if first else "elif"
                pat = self._expr(arm.pattern)
                guard = f" and ({self._expr(arm.guard)})" if arm.guard else ""
                self._w += f"{kw} {self._expr(node.subject)} == {pat}{guard}:"
                with self._w.indent():
                    if arm.body:
                        arm.body.accept(self)
                    elif arm.value:
                        self._w += f"_result = {self._expr(arm.value)}"
                    else:
                        self._w += "pass"
                first = False

    def visit_WhileStmt(self, node: WhileStmt):
        self._w += f"while {self._expr(node.condition)}:"
        with self._w.indent():
            node.body.accept(self)

    def visit_LoopStmt(self, node: LoopStmt):
        self._w += "while True:"
        with self._w.indent():
            node.body.accept(self)

    def visit_ForStmt(self, node: ForStmt):
        self._w += f"for {node.var} in {self._expr(node.iter)}:"
        with self._w.indent():
            node.body.accept(self)

    def visit_TryStmt(self, node: TryStmt):
        # Map common Lateralus error type names to Python exception types
        _exc_map = {
            "Error":          "Exception",
            "RuntimeError":   "RuntimeError",
            "ValueError":     "ValueError",
            "TypeError":      "TypeError",
            "IndexError":     "IndexError",
            "KeyError":       "KeyError",
            "IOError":        "IOError",
            "DivisionError":  "ZeroDivisionError",
            "OverflowError":  "OverflowError",
            "NotFoundError":  "FileNotFoundError",
            "AssertionError": "AssertionError",
        }
        self._w += "try:"
        with self._w.indent():
            node.body.accept(self)
        for clause in node.recoveries:
            py_exc = _exc_map.get(clause.error_type, clause.error_type or "Exception") \
                     if clause.error_type else "Exception"
            exc_decl = f"{py_exc} as {clause.binding}" if clause.binding else py_exc
            self._w += f"except {exc_decl}:"
            with self._w.indent():
                clause.body.accept(self)
        if node.ensure:
            self._w += "finally:"
            with self._w.indent():
                node.ensure.accept(self)

