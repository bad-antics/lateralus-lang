    def visit_LoopStmt(self, node):
        body_label  = self._builder.new_label("lbody")
        after_label = self._builder.new_label("lafter")
        self._break_labels.append(after_label)
        self._continue_labels.append(body_label)
        self._emit(IROp.JMP, dest=body_label)
        body_bb = self._new_bb(body_label)
        self._current_bb = body_bb
        self._emit(IROp.LABEL, dest=body_label)
        node.body.accept(self)
        self._emit(IROp.JMP, dest=body_label)
        after_bb = self._new_bb(after_label)
        self._current_bb = after_bb
        self._emit(IROp.LABEL, dest=after_label)
        self._break_labels.pop()
        self._continue_labels.pop()

    def visit_InterpolatedStr(self, node) -> str:
        # Build by concatenating string parts with IR ADD
        result_tmp = self._builder.new_tmp()
        self._emit(IROp.LOAD_STR, dest=result_tmp, src1="", span=node.span)
        for kind, val in node.parts:
            if kind == "str":
                str_tmp = self._builder.new_tmp()
                self._emit(IROp.LOAD_STR, dest=str_tmp, src1=val, span=node.span)
                new_tmp = self._builder.new_tmp()
                self._emit(IROp.ADD, dest=new_tmp, src1=result_tmp,
                           src2=str_tmp, span=node.span)
                result_tmp = new_tmp
            # expr parts: we store as a comment (full parsing would require
            # re-lexing the fragment, which is deferred to the runtime)
        return result_tmp

    def visit_RangeExpr(self, node) -> str:
        start_tmp = node.start.accept(self)
        end_tmp   = node.end.accept(self)
        if node.inclusive:
            adj_tmp = self._builder.new_tmp()
            self._emit(IROp.ADD, dest=adj_tmp, src1=end_tmp, src2=1,
                       span=node.span)
            end_tmp = adj_tmp
        tmp = self._builder.new_tmp()
        self._emit(IROp.CALL, dest=tmp, src1="range",
                   src2=[start_tmp, end_tmp], span=node.span)
        return tmp

