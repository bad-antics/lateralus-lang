    def visit_ImplBlock(self, node):
        # Emit each method as a named function prefixed with the type name
        for method in node.methods:
            qualified = FnDecl(
                span=method.span,
                name=f"{node.type_name}__{method.name}",
                params=method.params,
                ret_type=method.ret_type,
                body=method.body,
                is_async=method.is_async,
                is_pub=method.is_pub,
            )
            qualified.accept(self)

