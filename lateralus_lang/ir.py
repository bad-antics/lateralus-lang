    def visit_ForeignBlock(self, node) -> str:
        """Emit a FOREIGN_CALL IR instruction for a polyglot block."""
        tmp = self._builder.new_tmp()
        # Evaluate param expressions so they are in scope
        param_tmps = {p.name: p.value.accept(self) for p in node.params}
        self._emit(
            IROp.FOREIGN_CALL,
            dest=tmp,
            extra={"lang": node.lang, "source": node.source, "params": param_tmps},
            span=node.span,
        )
        return tmp



